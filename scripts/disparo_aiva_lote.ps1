Write-Host "Total unicos: $($unicos.Count)"

$URL = 'https://sdr-agente.vercel.app/api/sdr/send-initial'
$tamLote = 28
$totalSucesso = 0
$totalFalha = 0
$totalInvalidos = 0
$falhas = @()
$invalidos = @()

for ($i = 0; $i -lt $unicos.Count; $i += $tamLote) {
  $fim = [Math]::Min($i + $tamLote - 1, $unicos.Count - 1)
  $lote = $unicos[$i..$fim]
  $leads = @()
  foreach ($tel in $lote) {
    $leads += @{ nome = 'Lojista'; telefone = $tel; cidade = $null }
  }
  $body = @{ leads = $leads } | ConvertTo-Json -Depth 5 -Compress
  $loteNum = [Math]::Floor($i / $tamLote) + 1
  Write-Host ""
  Write-Host "=== Lote $loteNum ($($lote.Count) numeros, $($i+1)..$($fim+1) de $($unicos.Count)) ==="
  $t0 = Get-Date
  try {
    $r = Invoke-WebRequest -Uri $URL -Method POST -Body $body -ContentType 'application/json' -UseBasicParsing -TimeoutSec 280
    $j = $r.Content | ConvertFrom-Json
    $dt = [int]((Get-Date) - $t0).TotalSeconds
    Write-Host "  status: $($r.StatusCode) | tempo: ${dt}s | sucesso=$($j.sucesso) falha=$($j.falha) invalidos=$($j.invalidos)"
    $totalSucesso  += [int]$j.sucesso
    $totalFalha    += [int]$j.falha
    $totalInvalidos+= [int]$j.invalidos
    foreach ($res in $j.resultados) {
      if (-not $res.ok) {
        if ($res.invalido) { $invalidos += $res.telefone } else { $falhas += "$($res.telefone): $($res.erro)" }
      }
    }
  } catch {
    Write-Host "  ERRO: $($_.Exception.Message)" -ForegroundColor Red
    foreach ($tel in $lote) { $falhas += "${tel}: lote_falhou" }
  }
}

